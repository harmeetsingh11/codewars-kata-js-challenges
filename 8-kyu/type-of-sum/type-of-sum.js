function typeOfSum(a, b) {
  // Return the type of the sum of the two arguments using the `typeof` operator
  return typeof (a + b);
}

/*  
typeof Operator: The typeof operator returns a string indicating the type of the result of the addition:
"number" if the result is a number.
"string" if the result is a string.
"undefined", "object", etc., depending on the result type.
*/
