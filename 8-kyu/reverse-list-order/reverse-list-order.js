function reverseList(list) {
  // Use the built-in reverse method to reverse the array in-place
  return list.reverse();
}

/*
  Explanation:
  list.reverse():
  The reverse() method reverses the elements of an array in-place, which means it modifies the original array.
  It is both time-efficient (O(n) complexity) and memory-efficient because it doesn't create a new array.
  */
