function repeatString(n, s) {
  // Use the repeat method of the string object to repeat the string 'n' times
  return s.repeat(n);
}

/* 
Explanation:

Input Parameters:
n: The number of times the string s should be repeated.
s: The string to be repeated.

Key Method Used:
The .repeat(n) method is a built-in JavaScript function for strings. It repeats the string n times and concatenates the results.
*/
